// Definindo a classe Trip
class Trip {
    // Implementando o construtor da classe
    constructor(
      id,
      usuario,
      ano,
      estado,
      nome
    ) {
      this.id = id
      this.usuario = usuario
      this.ano = ano
      this.estado = estado
      this.nome = nome
    }
  }
  
  module.exports = Trip
  